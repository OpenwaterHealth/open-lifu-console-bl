/**
  * @brief Secure Engine Firmware Authentication Init function.
  *        The firmware tag (FwTag) is a SHA-256 digest of the clear firmware.
  * @param pxSE_Metadata Metadata (unused for a hash).
  * @param SE_FwType: Type of Fw Image (unused).
  * @retval SE_ErrorStatus SE_SUCCESS if successful, SE_ERROR otherwise.
  */
SE_ErrorStatus SE_CRYPTO_AuthenticateFW_Init(SE_FwRawHeaderTypeDef *pxSE_Metadata, uint32_t SE_FwType)
{
  (void)pxSE_Metadata;
  (void)SE_FwType;

  m_pSha256 = cmox_sha256_construct(&m_Sha256Ctx);
  if (m_pSha256 == NULL)
  {
    return SE_ERROR;
  }
  if (cmox_hash_init(m_pSha256) != CMOX_HASH_SUCCESS)
  {
    return SE_ERROR;
  }
  if (cmox_hash_setTagLen(m_pSha256, CMOX_SHA256_SIZE) != CMOX_HASH_SUCCESS)
  {
    return SE_ERROR;
  }
  return SE_SUCCESS;
}

/**
  * @brief Secure Engine Firmware Authentication Append function.
  * @param pInputBuffer: pointer to Input Buffer.
  * @param InputSize: Input Size (bytes).
  * @param pOutputBuffer: unused for a hash (kept for API compatibility).
  * @param pOutputSize: unused for a hash (kept for API compatibility).
  * @retval SE_ErrorStatus SE_SUCCESS if successful, SE_ERROR otherwise.
  */
SE_ErrorStatus SE_CRYPTO_AuthenticateFW_Append(const uint8_t *pInputBuffer, int32_t InputSize, uint8_t *pOutputBuffer,
                                               int32_t *pOutputSize)
{
  (void)pOutputBuffer;

  if ((pInputBuffer == NULL) || (InputSize < 0) || (m_pSha256 == NULL))
  {
    return SE_ERROR;
  }

  if (cmox_hash_append(m_pSha256, pInputBuffer, (size_t)InputSize) != CMOX_HASH_SUCCESS)
  {
    return SE_ERROR;
  }

  if (pOutputSize != NULL)
  {
    *pOutputSize = InputSize;
  }
  return SE_SUCCESS;
}

/**
  * @brief Secure Engine Firmware Authentication Finish function.
  * @param pOutputBuffer: pointer to Output Buffer (receives the SHA-256 digest).
  * @param pOutputSize: pointer to Output Size (bytes): 32.
  * @retval SE_ErrorStatus SE_SUCCESS if successful, SE_ERROR otherwise.
  */
SE_ErrorStatus SE_CRYPTO_AuthenticateFW_Finish(uint8_t *pOutputBuffer, int32_t *pOutputSize)
{
  size_t computed = 0U;

  if ((pOutputBuffer == NULL) || (pOutputSize == NULL) || (m_pSha256 == NULL))
  {
    return SE_ERROR;
  }

  if (cmox_hash_generateTag(m_pSha256, pOutputBuffer, &computed) != CMOX_HASH_SUCCESS)
  {
    (void)cmox_hash_cleanup(m_pSha256);
    m_pSha256 = NULL;
    return SE_ERROR;
  }
  (void)cmox_hash_cleanup(m_pSha256);
  m_pSha256 = NULL;

  if (computed != CMOX_SHA256_SIZE)
  {
    return SE_ERROR;
  }

  *pOutputSize = (int32_t)computed;
  return SE_SUCCESS;
}

/**
  * @brief Secure Engine header authentication function.
  *        Verifies the ECDSA P-256 signature (raw R||S) of the authenticated
  *        part of the firmware header against the embedded product public key,
  *        with a fault-injection double check (both return value and the
  *        fault_check out-param must equal CMOX_ECC_AUTH_SUCCESS).
  * @param pxSE_Metadata: pointer to the firmware header.
  * @retval SE_ErrorStatus SE_SUCCESS if successful, SE_ERROR otherwise.
  */
SE_ErrorStatus SE_CRYPTO_Authenticate_Metadata(SE_FwRawHeaderTypeDef *pxSE_Metadata)
{
  SE_ErrorStatus e_ret_status = SE_ERROR;
  cmox_ecc_handle_t ecc_ctx;
  cmox_ecc_retval_t retval;
  cmox_hash_retval_t hretval;
  uint32_t fault_check = CMOX_ECC_AUTH_FAIL;
  uint8_t digest[CMOX_SHA256_SIZE];
  size_t computed = 0U;

  if (pxSE_Metadata == NULL)
  {
    return SE_ERROR;
  }

  /* Retrieve the public key (raw X||Y, 64 bytes) from the protected key
   * functions generated from the product public key. */
  SE_CRYPTO_ReadKey_Pub(pxSE_Metadata, &(m_aSE_PubKey[0]));

  /* SHA-256 of the authenticated header region (everything before the
   * signature), one-shot. */
  hretval = cmox_hash_compute(CMOX_SHA256_ALGO,
                              (const uint8_t *)pxSE_Metadata, (size_t)SE_FW_AUTH_LEN,
                              digest, CMOX_SHA256_SIZE, &computed);
  if ((hretval != CMOX_HASH_SUCCESS) || (computed != CMOX_SHA256_SIZE))
  {
    (void)memset(digest, 0, sizeof(digest));
    return SE_ERROR;
  }

  /* ECDSA P-256 verification. Verify-only, low-memory curve variant + the
   * small math implementation to keep the CM0 footprint down. */
  cmox_ecc_construct(&ecc_ctx, CMOX_MATH_FUNCS_SMALL, m_EccWorkingBuffer, sizeof(m_EccWorkingBuffer));

  retval = cmox_ecdsa_verify(&ecc_ctx,
                             CMOX_ECC_SECP256R1_LOWMEM_VERIFY,
                             m_aSE_PubKey, SE_ASYM_PUBKEY_LEN,
                             digest, CMOX_SHA256_SIZE,
                             pxSE_Metadata->HeaderSignature, SE_HEADER_SIGN_LEN,
                             &fault_check);

  cmox_ecc_cleanup(&ecc_ctx);

  /* Accept ONLY if both the return value and the fault-check out-param report
   * success (anti fault-injection). Compare against SUCCESS, never FAIL. */
  if ((retval == CMOX_ECC_AUTH_SUCCESS) && (fault_check == CMOX_ECC_AUTH_SUCCESS))
  {
    e_ret_status = SE_SUCCESS;
  }

  /* Cleanup the digest scratch (defense in depth; the pub key is not secret) */
  (void)memset(digest, 0, sizeof(digest));

  return e_ret_status;
}


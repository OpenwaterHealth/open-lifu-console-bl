/* Private defines -----------------------------------------------------------*/
/* ST example value for a P-256 ECDSA verify working buffer. Trim with
 * cmox_membuf_getMaxUsed() only after measuring on-target. */
#define SE_ECC_WORKING_BUFFER_SIZE   2000U

/* Private variables ---------------------------------------------------------*/
/* These statics live in the dedicated SE RAM region (.se_bss) — see the linker
 * script — and are wiped by SFU_SE_RAM_Erase() before leaving the bootloader
 * (app launch or DFU entry). The SE RAM region is sized (4K) to hold them. */
static cmox_sha256_handle_t m_Sha256Ctx;                      /*!< FwTag digest context */
static uint8_t              m_aSE_PubKey[SE_ASYM_PUBKEY_LEN]; /*!< ECDSA public key scratch */
static uint8_t              m_EccWorkingBuffer[SE_ECC_WORKING_BUFFER_SIZE]; /*!< CMOX ECC membuf */

/* Handle returned by cmox_sha256_construct(); points into m_Sha256Ctx. */
static cmox_hash_handle_t  *m_pSha256;

/* Private functions ---------------------------------------------------------*/

/**
  * @brief Retrieve the ECDSA public key for signature verification.
  *        Single active image configuration: always key #1 (see se_key.s,
  *        generated by py-tools/gen_se_key_s.py from the product key pair).
  * @param pxSE_Metadata metadata of the image to authenticate (unused, single key)
  * @param paSE_PubKey destination buffer (SE_ASYM_PUBKEY_LEN = 64 bytes, raw X||Y)
  * @retval void
  */
